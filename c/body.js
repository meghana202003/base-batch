let newContainer = document.querySelector("#new-container");
let newDrop = document.querySelector("#new-drop");

newContainer.addEventListener("mouseover", function() {
    newDrop.style.display = "block";
});
newContainer.addEventListener("mouseleave", function() {
    newDrop.style.display = "none";
});


